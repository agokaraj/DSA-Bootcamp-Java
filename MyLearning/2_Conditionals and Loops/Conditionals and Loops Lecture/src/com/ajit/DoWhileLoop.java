package com.ajit;

public class DoWhileLoop {
    public static void main(String[] args) {
        int i=10;
        do{
            System.out.println("This will execute once even if the" +"\n"+" condition is true");
            System.out.println("This will execute once even if the \n condition is true");
        }
        while(i!=10);
        
    }
}
